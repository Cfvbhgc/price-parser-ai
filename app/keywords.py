"""Ключевые слова для распознавания колонок прайса.

Один словарь используется в двух местах:
  - при поиске строки-заголовка (какая строка похожа на «шапку»);
  - в эвристическом определении маппинга (какая колонка что означает),
    когда LLM недоступен.

Слова в нижнем регистре; сравнение — по вхождению подстроки, чтобы ловить формы
(«наименование товара», «цена, руб», «арт.»).
"""

from __future__ import annotations

# Порядок ключей = приоритет при конфликте (если ячейка похожа на несколько,
# берём первую подходящую категорию).
COLUMN_KEYWORDS: dict[str, list[str]] = {
    "name": ["наименование", "название", "номенклатура", "товар", "описание", "продукция"],
    "article": ["артикул", "sku", "код товара", "код", "арт."],
    "price": ["цена", "стоимость", "прайс", "розница", "опт"],
    "quantity": ["кол-во", "количество", "остаток", "наличие", "склад", "шт"],
}


def match_column_role(header_cell: str) -> str | None:
    """Определить роль колонки по тексту её заголовка (или None)."""
    text = header_cell.strip().lower()
    if not text:
        return None
    for role, words in COLUMN_KEYWORDS.items():
        if any(word in text for word in words):
            return role
    return None
